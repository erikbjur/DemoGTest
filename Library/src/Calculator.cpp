#include "Calculator.h"

int Library::Add( int a, int b ) {
    return a + b;
}

double Library::Add( double a, double b ) {
    return a + b;
}

int Library::Subtract( int a, int b ) {
    return a - b;
}

double Library::Subtract( double a, double b ) {
    return a - b;
}

int Library::Multiply( int a, int b ) {
    return a * b;
}

double Library::Multiply( double a, double b ) {
    return a * b;
}

double Library::Divide( double a, double b ) {
    return a / b;
}
